public class Ponto {
    private double x, y;

    public Ponto(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return ", coordenada x: "+this.getX() + ", coordenada y: " + this.getY(); //To change body of generated methods, choose Tools | Templates.
    }

}
